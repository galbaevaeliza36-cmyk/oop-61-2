# Библиотека colorama предназначена для цветного вывода текста в консоль
# Она позволяет делать сообщения более наглядными (ошибки, предупреждения, успех и т.д.)

from colorama import init, Fore, Style

# Инициализация colorama (обязательно для Windows)
init()

print(Fore.GREEN + "Успешное выполнение программы!")
print(Fore.YELLOW + "Это предупреждение.")
print(Fore.RED + "Это сообщение об ошибке.")
print(Style.RESET_ALL + "Цвета сброшены к стандартным.")